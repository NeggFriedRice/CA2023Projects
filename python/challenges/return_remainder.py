# There is a single operator in Python, capable of providing the print(remainder of a division operation. Two numbers are passed as parameters. The first parameter divided by the second parameter will have a print(remainder, possibly zero. Return that value.

def remainder(num1, num2):
    return num1 % num2

print(remainder(1, 3)) # 1

print(remainder(3, 4)) # 3

print(remainder(5, 5)) # 0

print(remainder(7, 2)) # 1